package com.a235;

public class DatatypeEx 
{
	
	public static void main(String[] args)
	{
		
		int a = 123;
		float b = 23.45F;
		char c= 'T' ;
		double d = 23.58789;
		String data ="Welcome to Tops Technologies 12345@@.*#";
		boolean b1 = true;
		
		
		System.out.println("This is Integer Value "+a);
		System.out.println("This is Float Value "+b);
		System.out.println("This is Character Value "+c);
		System.out.println("This is Double Value "+d);
		System.out.println("This is String Value "+data);
		System.out.println("This is Boolean Value "+b1);
	
	}
	
	
}
